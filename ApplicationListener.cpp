		gl->Vertex3f(-0.5f, -0.5f, 0.0f); // 좌하.
		gl->Color4f(1.0f, 0.0f, 0.0f, 1.0f);
		gl->Vertex3f(0.5f, -0.5f, 0.0f); // 우하.
